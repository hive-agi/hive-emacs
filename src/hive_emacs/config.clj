(defconfig EmacsConfig
  :emacsclient-path (env "EMACSCLIENT"
                         :default "emacsclient"
                         :type :string
                         :doc "Path to emacsclient binary")
  :socket-name      (env "EMACS_SOCKET_NAME"
                         :type :string
                         :required false
                         :doc "Emacs daemon socket name. nil = default daemon (no -s flag)."))

(def ^:const default-socket-fallback
  "Daemon ID used when EMACS_SOCKET_NAME is unset.
   Matches the conventional Emacs daemon name when started without --daemon=NAME."
  "server")

(defn resolve!
  "Resolve EmacsConfig with optional overrides.
   Returns the resolved map directly. Throws if resolution fails
   (defaults guarantee resolution under normal conditions)."
  ([] (resolve! {}))
  ([overrides]
   (let [result (resolve-EmacsConfig overrides)]
     (if (r/ok? result)
       (:ok result)
       (throw (ex-info "EmacsConfig resolution failed"
                       {:result result :overrides overrides}))))))

(defn emacsclient-path
  "Resolved emacsclient binary path."
  []
  (:emacsclient-path (resolve!)))

(defn socket-name
  "Resolved EMACS_SOCKET_NAME (may be nil for default daemon)."
  []
  (:socket-name (resolve!)))

(defn default-daemon-id
  "Daemon ID for registration: socket-name when set, else fallback (\"server\").
   Used by daemon-store and daemon-selection to identify the canonical daemon."
  []
  (or (socket-name) default-socket-fallback))
